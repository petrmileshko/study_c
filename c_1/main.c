/**
 * Программа вывода на экран в консоли строки "Hello world"
 */
#include <stdio.h> //подключение стандартной библиотеки (необходима для функции printf)

int main () // определение функции main - основная функция для запуска программы
{
    printf("Hello world!\n"); // библиотечная функция вывода на экран
    // \n - это управляющая последовательность для перевода строки на новую
}
