#!/bin/bash

echo "=== Запуск тестов для программы подсчёта символов ==="

# Тест 1: Простой текст
echo -e "Test line one\nTest line two\x18" > test1.in
echo "Тест 1"
echo "Ожидаемый результат: символов 27, строк 2, слов 6" 
./bin/c_5 < test1.in
echo "----------------------------------------"

# Тест 2: Пустой ввод
echo -e "\x18" > test2.in
echo "Тест 2"
echo "Ожидаемый результат: символов 0, строк 0, слов 0"
./bin/c_5 < test2.in
echo "----------------------------------------"

# Тест 3: Одно слово
echo -e "Hello\x18" > test3.in
echo "Тест 3"
echo "Ожидаемый результат: символов 5, строк 1, слов 1"
./bin/c_5 < test3.in
echo "----------------------------------------"

# Тест 4: Несколько пустых строк
echo -e "\n\n\n\x18" > test4.in
echo "Тест 4"
echo "Ожидаемый результат: символов 3, строк 3, слов 0"
./bin/c_5 < test4.in
echo "----------------------------------------"

# Тест 5: Одно слово и управляющие символы
echo -e " \n\t\n hi\x18" > test5.in
echo "Тест 5"
echo "Ожидаемый результат: символов 7, строк 3, слов 1"
./bin/c_5 < test5.in
echo "----------------------------------------"

# Тест 6: Незаконченная строка
echo -e "\thello \x18" > test6.in
echo "Тест 6"
echo "Ожидаемый результат: символов 7, строк 1, слов 1"
./bin/c_5 < test6.in
echo "========================================"
